-- ---------- Live chat ----------
-- Visitors must sign in (email/password or Google) to chat. The admin account(s)
-- that can see every conversation are listed in `admins`.
--
-- After creating your admin login user (Authentication -> Users -> Add user,
-- email admin@screenprintingbali.com), copy that user's UUID and run:
--   insert into admins (user_id) values ('<paste-uuid-here>');

create table if not exists admins (
  user_id uuid primary key references auth.users(id) on delete cascade
);

alter table admins enable row level security;

create policy "admin reads own admin row" on admins for select
  using (user_id = auth.uid());

create or replace function is_admin()
returns boolean
language sql
security definer
set search_path = public
stable
as $$
  select exists (select 1 from admins where user_id = auth.uid());
$$;

create table if not exists conversations (
  id uuid primary key default gen_random_uuid(),
  visitor_id uuid not null references auth.users(id) on delete cascade,
  visitor_email text,
  status text not null default 'open' check (status in ('open', 'closed')),
  created_at timestamptz not null default now(),
  last_message_at timestamptz not null default now(),
  unique (visitor_id)
);

create table if not exists messages (
  id bigint generated by default as identity primary key,
  conversation_id uuid not null references conversations(id) on delete cascade,
  sender text not null check (sender in ('visitor', 'admin')),
  sender_id uuid not null references auth.users(id),
  body text not null check (char_length(trim(body)) > 0),
  read_by_admin boolean not null default false,
  read_by_visitor boolean not null default true,
  created_at timestamptz not null default now()
);

create index if not exists messages_conversation_id_idx on messages (conversation_id, created_at);

alter table conversations enable row level security;
alter table messages enable row level security;

create policy "visitor or admin read own conversation" on conversations for select
  using (visitor_id = auth.uid() or is_admin());

create policy "visitor creates own conversation" on conversations for insert
  with check (visitor_id = auth.uid());

create policy "visitor or admin update own conversation" on conversations for update
  using (visitor_id = auth.uid() or is_admin());

create policy "visitor or admin read own messages" on messages for select
  using (
    is_admin()
    or exists (
      select 1 from conversations c
      where c.id = messages.conversation_id and c.visitor_id = auth.uid()
    )
  );

create policy "visitor or admin send messages" on messages for insert
  with check (
    sender_id = auth.uid()
    and (
      (sender = 'admin' and is_admin())
      or (
        sender = 'visitor'
        and exists (
          select 1 from conversations c
          where c.id = messages.conversation_id and c.visitor_id = auth.uid()
        )
      )
    )
  );

create policy "visitor or admin mark messages read" on messages for update
  using (
    is_admin()
    or exists (
      select 1 from conversations c
      where c.id = messages.conversation_id and c.visitor_id = auth.uid()
    )
  );

-- Keep conversations.last_message_at in sync and set read flags based on sender.
create or replace function chat_message_defaults()
returns trigger
language plpgsql
as $$
begin
  if new.sender = 'admin' then
    new.read_by_admin := true;
    new.read_by_visitor := false;
  else
    new.read_by_admin := false;
    new.read_by_visitor := true;
  end if;
  return new;
end;
$$;

drop trigger if exists trg_chat_message_defaults on messages;
create trigger trg_chat_message_defaults
  before insert on messages
  for each row execute function chat_message_defaults();

create or replace function touch_conversation()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  update conversations
  set last_message_at = new.created_at,
      status = 'open'
  where id = new.conversation_id;
  return new;
end;
$$;

drop trigger if exists trg_touch_conversation on messages;
create trigger trg_touch_conversation
  after insert on messages
  for each row execute function touch_conversation();

