-- Screenprinting Bali pricing calculator schema
-- Run this in the Supabase SQL editor (or via `supabase db push`).

create table if not exists products (
  slug text primary key,
  label text not null,
  has_cut_option boolean not null default false,
  has_bag_size_option boolean not null default false,
  moq integer not null default 1,
  sort_order integer not null default 0
);

create table if not exists fabrics (
  id bigint generated by default as identity primary key,
  product_slug text not null references products(slug) on delete cascade,
  value text not null,
  label text not null,
  price numeric not null,
  sort_order integer not null default 0,
  unique (product_slug, value)
);

create table if not exists cuts (
  slug text primary key,
  label text not null,
  multiplier numeric not null default 1,
  sort_order integer not null default 0
);

create table if not exists bag_sizes (
  slug text primary key,
  label text not null,
  dim text not null,
  multiplier numeric not null default 1,
  sort_order integer not null default 0
);

create table if not exists print_methods (
  slug text primary key,
  label text not null,
  type text not null check (type in ('dtf', 'screen')),
  moq integer not null default 1,
  -- DTF fields
  film_rate_per_cm2 numeric,
  press_margin numeric,
  press_flat_cost numeric,
  -- Screen (plastisol/rubber) fields
  base_cost numeric,
  per_extra_color numeric,
  setup_per_color numeric,
  -- Which product slugs this method applies to. NULL = available to all products.
  applicable_products text[],
  sort_order integer not null default 0
);

create table if not exists design_sizes (
  slug text primary key,
  label text not null,
  dim text not null,
  area_cm2 numeric not null,
  multiplier numeric not null default 1,
  sort_order integer not null default 0
);

